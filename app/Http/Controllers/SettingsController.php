	 * Store a newly created resource in storage.
	 *
	 * @return Response
	 */
	public function store()
	{
		$input = Input::all();

		foreach ($input as $key => $value) {
			if ($key != '_token') {
				Cache::forever('settings_'.$key, $value);
			}
		}

		return redirect('admin/settings');
	}

